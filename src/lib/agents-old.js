export function getAgentById(id) {
  return agents.find(agent => agent.id === id);
}

export function getActiveAgents() {
  return agents.filter(agent => agent.status === 'active');
}

export function getAgentsByDomain(domain) {
  return agents.filter(agent => agent.domain === domain);
}

export function getAgentDomains() {
  return [...new Set(agents.map(agent => agent.domain))];
}

export function getAgentStats() {
  const totalTasks = agents.reduce((sum, agent) => sum + agent.tasks, 0);
  const avgAccuracy = agents.reduce((sum, agent) => sum + agent.accuracy, 0) / agents.length;
  const avgResponseTime = agents.reduce((sum, agent) => sum + agent.responseTime, 0) / agents.length;
  const avgUptime = agents.reduce((sum, agent) => sum + agent.uptime, 0) / agents.length;
  const activeCount = getActiveAgents().length;
  
  return {
    totalAgents: agents.length,
    activeAgents: activeCount,
    totalTasks,
    avgAccuracy: Math.round(avgAccuracy * 10) / 10,
    avgResponseTime: Math.round(avgResponseTime * 100) / 100,
    avgUptime: Math.round(avgUptime * 10) / 10
  };
}
